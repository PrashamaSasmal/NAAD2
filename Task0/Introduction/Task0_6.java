//WAP to Swap Two Numbers
public class Task0_6 {
    public static void main(String args[]){
    int num1=20,num2=30;
    System.out.println("Numbers before Swapping: "+num1+" "+num2);
    int temp=num1;
    num1=num2;
    num2=temp;
    System.out.println("Numbers after Swapping: "+num1+" "+num2);
   
}
    
}
